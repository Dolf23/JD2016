package by.it.telushko.jd02_04;


public class Parser {
    public static double add (double a, double b)  {
        double c=a+b;
        return c;
    }
    public static double sub (double a, double b)  {
        double c=a-b;
        return c;
    }
    public static double mul (double a, double b)  {
        double c=a*b;
        return c;
    } public static double div (double a, double b)  {
        double c=a/+b;
        return c;
    }
}
